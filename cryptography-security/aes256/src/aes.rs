// Round constants used in AES key expansion
pub const RCON: [u8; 11] = [
    0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36,
];

// Multiply a byte by 2 in GF(2^8)
fn xtime(b: u8) -> u8 {
    if b & 0x80 != 0 {
        (b << 1) ^ 0x1b
    } else {
        b << 1
    }
}

// Multiply two bytes in GF(2^8)
fn gf_mul(a: u8, b: u8) -> u8 {
    let mut result = 0u8;
    let mut a = a;
    let mut b = b;

    while b > 0 {
        if b & 1 != 0 {
            result ^= a;
        }
        a = xtime(a);
        b >>= 1;
    }
    result
}

// Substitute each byte in the state using the S-Box
pub fn sub_bytes(state: &mut [u8; 16]) {
    for byte in state.iter_mut() {
        *byte = SBOX[*byte as usize];
    }
}

// Inverse SubBytes using the inverse S-Box (used in decryption)
pub fn sub_bytes_inv(state: &mut [u8; 16]) {
    for byte in state.iter_mut() {
        *byte = SBOX_INV[*byte as usize];
    }
}

// Shift rows of the state matrix to the left
pub fn shift_rows(state: &mut [u8; 16]) {
    // Row 1: shift left by 1
    let tmp = state[1];
    state[1] = state[5];
    state[5] = state[9];
    state[9] = state[13];
    state[13] = tmp;

    // Row 2: shift left by 2
    state.swap(2, 10);
    state.swap(6, 14);

    // Row 3: shift left by 3
    let tmp = state[15];
    state[15] = state[11];
    state[11] = state[7];
    state[7] = state[3];
    state[3] = tmp;
}

// Inverse ShiftRows: shift rows to the right (used in decryption)
pub fn shift_rows_inv(state: &mut [u8; 16]) {
    // Row 1: shift right by 1
    let tmp = state[1];
    state[1] = state[13];
    state[13] = state[9];
    state[9] = state[5];
    state[5] = tmp;

    // Row 2: shift right by 2
    state.swap(2, 10);
    state.swap(6, 14);

    // Row 3: shift right by 3
    let tmp = state[3];
    state[3] = state[7];
    state[7] = state[11];
    state[11] = state[15];
    state[15] = tmp;
}

// Mix each column of the state using GF(2^8) multiplication
pub fn mix_columns(state: &mut [u8; 16]) {
    for i in 0..4 {
        let s0 = state[i * 4];
        let s1 = state[i * 4 + 1];
        let s2 = state[i * 4 + 2];
        let s3 = state[i * 4 + 3];

        state[i * 4] = gf_mul(0x02, s0) ^ gf_mul(0x03, s1) ^ s2 ^ s3;
        state[i * 4 + 1] = s0 ^ gf_mul(0x02, s1) ^ gf_mul(0x03, s2) ^ s3;
        state[i * 4 + 2] = s0 ^ s1 ^ gf_mul(0x02, s2) ^ gf_mul(0x03, s3);
        state[i * 4 + 3] = gf_mul(0x03, s0) ^ s1 ^ s2 ^ gf_mul(0x02, s3);
    }
}

// Inverse MixColumns (used in decryption)
pub fn mix_columns_inv(state: &mut [u8; 16]) {
    for i in 0..4 {
        let s0 = state[i * 4];
        let s1 = state[i * 4 + 1];
        let s2 = state[i * 4 + 2];
        let s3 = state[i * 4 + 3];

        state[i * 4] = gf_mul(0x0e, s0) ^ gf_mul(0x0b, s1) ^ gf_mul(0x0d, s2) ^ gf_mul(0x09, s3);
        state[i * 4 + 1] = gf_mul(0x09, s0) ^ gf_mul(0x0e, s1) ^ gf_mul(0x0b, s2) ^ gf_mul(0x0d, s3);
        state[i * 4 + 2] = gf_mul(0x0d, s0) ^ gf_mul(0x09, s1) ^ gf_mul(0x0e, s2) ^ gf_mul(0x0b, s3);
        state[i * 4 + 3] = gf_mul(0x0b, s0) ^ gf_mul(0x0d, s1) ^ gf_mul(0x09, s2) ^ gf_mul(0x0e, s3);
    }
}

// Expand the 32-byte key into 15 round keys (240 bytes total)
pub fn key_expansion(key: &[u8; 32]) -> [u8; 240] {
    let mut expanded = [0u8; 240];

    // Copy original key into the first 32 bytes
    expanded[..32].copy_from_slice(key);

    let mut i = 8;

    while i < 60 {
        let mut temp = [
            expanded[(i - 1) * 4],
            expanded[(i - 1) * 4 + 1],
            expanded[(i - 1) * 4 + 2],
            expanded[(i - 1) * 4 + 3]
        ];

        if i % 8 == 0 {
            // RotWord: rotate left by 1 byte
            temp.rotate_left(1);

            // SubWord: apply S-Box to each byte
            for byte in temp.iter_mut() {
                *byte = SBOX[*byte as usize];
            }

            // XOR with round constant
            temp[0] ^= RCON[i / 8];
        } else if i % 8 == 4 {
            // Extra SubWord for AES-256
            for byte in temp.iter_mut() {
                *byte = SBOX[*byte as usize];
            }
        }

        // XOR with word 8 positions back
        for j in 0..4 {
            expanded[i * 4 + j] = expanded[(i - 8) * 4 + j] ^ temp[j];
        }
        i += 1;
    }
    expanded
}

// XOR the state with the current round key
pub fn add_round_key(state: &mut [u8; 16], round_key: &[u8]) {
    for (i, byte) in state.iter_mut().enumerate() {
        *byte ^= round_key[i];
    }
}

// Encrypt a single 16-byte block using AES-256
pub fn encrypt_block(block: &[u8; 16], expanded_key: &[u8; 240]) -> [u8; 16] {
    let mut state = *block;

    // Initial round key addition
    add_round_key(&mut state, &expanded_key[0..16]);

    // Main rounds (14 rounds for AES-256, last one without MixColumns)
    for round in 1..14 {
        sub_bytes(&mut state);
        shift_rows(&mut state);
        mix_columns(&mut state);
        add_round_key(&mut state, &expanded_key[round * 16..(round + 1) * 16]);
    }

    // Final round
    sub_bytes(&mut state);
    shift_rows(&mut state);
    add_round_key(&mut state, &expanded_key[224..240]);

    state
}

// Decrypt a single 16-byte block using AES-256
pub fn decrypt_block(block: &[u8; 16], expanded_key: &[u8; 240]) -> [u8; 16] {
    let mut state = *block;

    // Initial round key addition (reverse order)
    add_round_key(&mut state, &expanded_key[224..240]);

    // Main rounds in reverse
    for round in (1..14).rev() {
        shift_rows_inv(&mut state);
        sub_bytes_inv(&mut state);
        add_round_key(&mut state, &expanded_key[round * 16..(round + 1) * 16]);
        mix_columns_inv(&mut state);
    }

    // Final round
    shift_rows_inv(&mut state);
    sub_bytes_inv(&mut state);
    add_round_key(&mut state, &expanded_key[0..16]);

    state
}
